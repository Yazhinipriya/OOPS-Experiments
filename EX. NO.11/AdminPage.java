  public void comboboxes() {
        Connection c;
        try {
            jComboBox2.removeAllItems();
            jComboBox3.removeAllItems();
            jComboBox4.removeAllItems();
            c = DriverManager.getConnection("jdbc:derby://localhost:1527/ebookshop");
            Statement st = c.createStatement();
            ResultSet rs = st.executeQuery("Select * from books");
            while (rs.next()) {
                jComboBox3.addItem(rs.getString("title"));
                jComboBox2.addItem(rs.getString("title"));
                jComboBox4.addItem(String.valueOf(rs.getInt("ID")));
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }
